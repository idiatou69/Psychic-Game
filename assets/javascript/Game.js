var win = 0;
var lose = 0;
var left = 9;
var alf = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k",
    "l", "m", "n", "o", "p", "q", "r", "s", "t", "u", "v", "w", "x", "y", "z"];
//creating an array of all alphabet letters
// the alf array will be used to associate the random number that computer picks with a letter
document.getElementById("win").innerHTML = "win" + win;
document.getElementById("lose").innerHTML = "lose" + lose;
document.getElementById("left").innerHTML = "guess left:" + left;
document.getElementById("far").innerHTML = "your guess so  far:";
// function called at key pressing event

document.onkeyup = function () {
    //    variable compGuess  will hold a letter from the array using the index ran, a number generated by the math floor function.
    // document.onkeyup=function(){
    var compGuess = "";
    var ran = Math.floor(Math.random() * 26);
    console.log(ran);
    compGuess = alf[ran];
    console.log(compGuess);
    // variable guess will get the value of the pressd key using the proprety key of the event object
    var guess = event.key;
    console.log(guess);
    // concatenate the value of the key pressed to the text of the element with the "far" class
    // element <span> wih the "far" class will display the letters picked by the player
    // after every  key pressed the number of guesses left decrement by 1
    document.getElementById("far").innerHTML += guess;
    left--;

    // comparing the letter picked by the player (guess) to compGuess the letter picked randomly by the computer
    // if the values are equal the player wins
    // if the number of guesses left equals 0 the player loses
    if (guess == compGuess) {
        win++;
        alert("you won, your winning letter is " + guess);
        left = 9;
        document.getElementById("far").innerHTML = "your guesses so far: ";
    }
    
    if (left == 0) {
        lose++;
        alert("you lost");
        left = 9;
        document.getElementById("far").innerHTML = "your guesses so far: ";
    }
    
    // displaying the counters at the html page.
    document.getElementById("win").innerHTML = "win " + win;
    document.getElementById("lose").innerHTML = "lose " + lose;
    document.getElementById("left").innerHTML = "guesses left  " + left;
}
    